    this.updateView(this.debugItem[0]);
  },
  methods: {
    handleDebugUiClick(evt) {
      const id = evt.target.dataset.id;
      this.updateView(this.debugItem[id]);
    },
    updateView(viewData) {
      this.$store.commit('updateCurrentView', viewData);
    },
    handleCanvasResponse(string) {
      switch (string) {
        case 'Start':
          // From welcome to onboarding
          this.updateView(this.debugItem[1]);
          break;
        case 'Settings':
          // Open Settings screen
          this.updateView(this.debugItem[5]);
          break;
        case 'Game':
          // Open Game Scene
          this.updateView(this.debugItem[2]);
          break;
      }
    },
  },
};
